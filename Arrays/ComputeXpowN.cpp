/* To compute x^n using Binary Exponentiation */

#include <iostream>
using namespace std;

double findPow(double x, int n) {

    if (n == 0) return 1.0;
    if (x == 0) return 0.0;
    if (x == 1) return 1.0;

    if (x == -1 && n % 2 == 0) return 1.0;
    if (x == -1 && n % 2 != 0) return -1.0;

    long binform = n;
    if (binform < 0) {
        x = 1 / x;
        binform = -binform;
    }

    double ans = 1.0;
    while (binform > 0) {
        if (binform % 2 == 1) {
            ans *= x;
        }
        x *= x;
        binform /= 2;
    }
    return ans;
}

int main() {
    double x = 7;
    int n = 5;

    double result = findPow(x, n);
    cout << x << " to the power " << n << " is " << result;

    return 0;
}
